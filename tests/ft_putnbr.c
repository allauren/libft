#include "libft.h"

void	ft_putnbr(int nb)
{
	long i;

	i = 1;
	if (nb == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (nb < 0)
		{
			nb = -nb;
			ft_putchar('-');
		}
		while (i <= nb)
			i = i * 10;
		while (i > 10)
		{
			i = i / 10;
			ft_putchar(nb / i + '0');
			nb = nb % i;
		}
		ft_putchar(nb % 10 + '0');
	}
}
