int		ft_str_is_numeric(char *str)
{
	int c;

	c = 0;
	while ((str[c] >= '0' && str[c] <= '9') || str[0] == '\0')
	{
		c++;
		if (str[c] == '\0')
			return (1);
	}
	return (0);
}
